// Comentário de linha simples
console.log("Hello, World!");

/* Comentário multilinha simples */
let x = 5;

/*
   Comentário multilinha
   com várias linhas
   explicando o código
*/
let y = 10;

// Variável com comentário inline
let z = x + y; // Esta é a soma de x e y

console.log(z); // Exibe o resultado

/* 
   Função com comentários
*/
function soma(a, b) {
  return a + b; // Retorna a soma
}

// Testando a função
let resultado = soma(5, 3); // Chamando a função
console.log(resultado); // Exibindo resultado
